def find_larger_number (a, b):
    return a if a>b else b
"""
Compares two numbers and returns the larger of the two.

Parameters:
a (int or float): The first number to compare.
b (int or float): The second number to compare.

Returns:
int or float: The larger of the two numbers.

"""

result = find_larger_number(10,20)
print("The larger number:", result)


